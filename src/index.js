const path = require('path');
require('dotenv').config();

const EnvironmentConfig = require('./config/EnvironmentConfig');
const FileSessionStorage = require('./core/FileSessionStorage');
const JsonFileRepository = require('./repositories/JsonFileRepository');
const RegisteredGuestRepository = require('./repositories/RegisteredGuestRepository');
const ProductCatalogRepository = require('./repositories/ProductCatalogRepository');
const CartRepository = require('./repositories/CartRepository');
const TelegramHtmlFormatter = require('./formatters/TelegramHtmlFormatter');
const NameContentPolicyService = require('./services/NameContentPolicyService');
const LoungeFormatValidationService = require('./services/LoungeFormatValidationService');
const RestaurantTableValidationService = require('./services/RestaurantTableValidationService');
const UserOnboardingService = require('./services/UserOnboardingService');
const VipGuestMenuService = require('./services/VipGuestMenuService');
const RegisteredGuestService = require('./services/RegisteredGuestService');
const ProductCatalogAdminService = require('./services/ProductCatalogAdminService');
const ProductCatalogGuestService = require('./services/ProductCatalogGuestService');
const GuestCatalogDeliveryService = require('./services/GuestCatalogDeliveryService');
const CartService = require('./services/CartService');
const CartDeleteDialogService = require('./services/CartDeleteDialogService');
const UserShoppingFlowService = require('./services/UserShoppingFlowService');
const AdminProductPayloadParser = require('./services/AdminProductPayloadParser');
const AdminCommandService = require('./services/AdminCommandService');
const AdminPanelService = require('./services/AdminPanelService');
const AdminProductDialogService = require('./services/AdminProductDialogService');
const AdminProductQuantityDialogService = require('./services/AdminProductQuantityDialogService');
const AdminBroadcastDialogService = require('./services/AdminBroadcastDialogService');
const VipTelegramBot = require('./bot/VipTelegramBot');

const config = new EnvironmentConfig(process.env), formatter = new TelegramHtmlFormatter();
const usersRepo = new RegisteredGuestRepository(new JsonFileRepository(path.join(__dirname, '../data/users.json'))), productsRepo = new ProductCatalogRepository(new JsonFileRepository(path.join(__dirname, '../data/products.json'))), cartRepo = new CartRepository(new JsonFileRepository(path.join(__dirname, '../data/carts.json')));
const sessionStorage = new FileSessionStorage(new JsonFileRepository(path.join(__dirname, '../data/sessions.json')));
const onboardingService = new UserOnboardingService(sessionStorage, formatter, new NameContentPolicyService(), new LoungeFormatValidationService(), new RestaurantTableValidationService());
const guestRegistryService = new RegisteredGuestService(usersRepo), productService = new ProductCatalogAdminService(productsRepo);
const adminDialog = new AdminProductDialogService(config.getAdminId(), sessionStorage, productService), quantityDialog = new AdminProductQuantityDialogService(config.getAdminId(), sessionStorage, productService), broadcastDialog = new AdminBroadcastDialogService(config.getAdminId(), sessionStorage, guestRegistryService);
const adminCommands = new AdminCommandService(config.getAdminId(), guestRegistryService, productService, new AdminProductPayloadParser(), new AdminPanelService(), adminDialog, quantityDialog, broadcastDialog);
const cartService = new CartService(cartRepo, productsRepo), cartDeleteDialog = new CartDeleteDialogService(sessionStorage, cartService);
const catalogDelivery = new GuestCatalogDeliveryService(new ProductCatalogGuestService(productsRepo, formatter));
const userShopping = new UserShoppingFlowService(catalogDelivery, cartService, guestRegistryService, config.getAdminId());
const bot = new VipTelegramBot(config.getBotToken(), onboardingService, new VipGuestMenuService(formatter), guestRegistryService, adminCommands, adminDialog, quantityDialog, broadcastDialog, cartDeleteDialog, userShopping, config.getAdminId());

bot.startPolling();
